namespace CSharp_RayTracer
{
    public class Colour
    {
        public int r;
        public int g;
        public int b;

        public Colour()
        {
            r = 0;
            g = 0;
            b = 0;
        }

        public Colour(int _r, int _g, int _b)
        {
            r = _r;
            g = _g;
            b = _b;
        }
    }
}